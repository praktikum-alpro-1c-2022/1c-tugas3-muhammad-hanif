#include <iostream>
using namespace std;

int main (){
  //Menghitung Variabel
  int x = 15, b = 25, c = 20;
  float Y;

  cout << "Nilai x = " << x << endl;
  cout << "Nilai b = " << b << endl;
  cout << "Nilai c = " << c << endl;

  //rumus
  Y = b*x*x + 0.5*x - c;
  cout << "Nilai Y = "<< Y << endl;


  return 0;
}